


template<typename cal>
cal sum(cal num1, cal num2)
{
    return num1+num2;
}
cal sub(cal num1, cal num2)
{
    return num1-num2;
}
cal multiply(cal num1, cal num2)
{
    return num1*num2;
}
cal division(cal num1, cal num2)
{
    return num1/num2;
}

